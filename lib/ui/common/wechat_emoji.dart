/// 把文本里的微信内置表情名 `[xxx]` 还原成 emoji。
///
/// 只替换映射表里有的；未知的 `[xxx]` 原样保留（可能是别的占位符，不强行处理）。
/// 兼容全/半角方括号。
String convertWeChatEmoji(String text) {
  if (!text.contains('[') && !text.contains('【')) return text;
  return text.replaceAllMapped(
    RegExp(r'[\[【]([^\[\]【】]{1,8})[\]】]'),
    (m) {
      final name = m.group(1)!.trim();
      return kWeChatEmoji[name] ?? m.group(0)!;
    },
  );
}
